package kim_start.loop;

public class Ex1 {
	public static void main(String[] args) {
		
		//While
		int count = 1;
		while (count <= 10) {
			System.out.println(count);
			count++;
		}
		
		//for
		for(int count1 = 1; count1 <= 10; count1++) {
			System.out.println(count1);
		}
	}
}
